    <h3>Госпитализация </h3>
    В назначенный день пациент приходит с направлением, с необходимым минимумом анализов, с полисом и паспортом к врачу-консультанту. Здесь заводится история болезни, проводится первичный осмотр пациента. Затем служба сопровождения приемного отделения провожает пациента в отделение, в палату. В отделении, после осмотра пациента лечащим врачом совместно с заведующим отделения, назначается лечение и план обследования пациента. При выписке пациент получает выписку с рекомендациями.
