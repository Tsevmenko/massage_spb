    <h3>Этапы поступления на бюджетной основе: </h3>
    Пациент с направлением из лечебно-профилактических учреждений г. Казани и Республики Татарстан приходит в приемное отделение МКДЦ, в столе справок получает пропуск и проходит в регистратуру приемного отделения к регистраторам, где его заносят в базу данных и направляют к профильному врачу-консультанту. Врач определяет обоснованность направления и госпитализации. В случае обоснованности направления, врач-консультант записывает пациента на госпитализацию на определенную дату (по очереди в лист ожидания).
